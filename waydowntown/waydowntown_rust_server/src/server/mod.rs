/// Request parser for `Api`.
pub struct ApiRequestParser;
impl<T> RequestParser<T> for ApiRequestParser {
    fn parse_operation_id(request: &Request<T>) -> Option<&'static str> {
        let path = paths::GLOBAL_REGEX_SET.matches(request.uri().path());
        match *request.method() {
            // AnswersPost - POST /answers
            hyper::Method::POST if path.matched(paths::ID_ANSWERS) => Some("AnswersPost"),
            // GamesIdGet - GET /games/{id}
            hyper::Method::GET if path.matched(paths::ID_GAMES_ID) => Some("GamesIdGet"),
            // GamesPost - POST /games
            hyper::Method::POST if path.matched(paths::ID_GAMES) => Some("GamesPost"),
            _ => None,
        }
    }
}
